export function useTranslation() {
  const getLanguage = () => {
    if (typeof window !== 'undefined') {
      return localStorage.getItem('language') || 'vi'
    }
    return 'vi'
  }

  const language = getLanguage() as 'vi' | 'en'
  return translations[language]
}
